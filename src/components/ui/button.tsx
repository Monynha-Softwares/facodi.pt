import { Slot } from '@radix-ui/react-slot';
import { cva, type VariantProps } from 'class-variance-authority';
import * as React from 'react';

import { cn } from '@/lib/utils';

const buttonVariants = cva(
  'inline-flex items-center justify-center gap-2 rounded-2xl border border-transparent bg-primary px-5 py-2.5 text-sm font-semibold text-primary-foreground shadow-soft transition-all focus-visible:outline-none focus-visible:ring-4 focus-visible:ring-focus/60 focus-visible:ring-offset-2 focus-visible:ring-offset-background disabled:pointer-events-none disabled:opacity-60',
  {
    variants: {
      variant: {
        default: 'bg-primary text-primary-foreground hover:shadow-soft hover:brightness-110',
        outline:
          'border-border bg-transparent text-foreground shadow-none hover:border-primary hover:text-primary focus-visible:ring-border',
        secondary:
          'bg-secondary text-secondary-foreground hover:shadow-soft hover:brightness-110 focus-visible:ring-secondary/50'
      },
      size: {
        default: 'px-5 py-2.5 text-sm',
        sm: 'px-3 py-2 text-xs',
        lg: 'px-6 py-3 text-base'
      }
    },
    defaultVariants: {
      variant: 'default',
      size: 'default'
    }
  }
);

export interface ButtonProps
  extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
  asChild?: boolean;
}

export const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, asChild = false, ...props }, ref) => {
    const Comp = asChild ? Slot : 'button';
    return (
      <Comp className={cn(buttonVariants({ variant, size, className }))} ref={ref} {...props} />
    );
  }
);
Button.displayName = 'Button';

export { buttonVariants };
