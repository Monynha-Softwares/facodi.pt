def _quote(value) -> str:
    if value is None:
        return ""
    return str(value).replace('\\', '\\\\').replace('"', '\\"')


def write_uc(uc):
    dir_path = os.path.join(BASE_DIR, uc["slug"])
    os.makedirs(dir_path, exist_ok=True)
    title = _quote(uc["title"]).strip()
    description = _quote(uc["description"]).strip()
    summary = _quote(uc["summary"]).strip()
    fm_lines = [
        "---",
        f'title: "{title}"',
        f'code: "{uc["code"]}"',
        f'description: "{description}"',
        f"ects: {uc['ects']}",
        f'semester: {uc["semester"]}',
        'language: "pt"',
    ]
    fm_lines.append("prerequisites: []")
    fm_lines.append("learning_outcomes:")
    for outcome in uc["learning_outcomes"]:
        fm_lines.append(f'  - "{_quote(outcome)}"')
    fm_lines.append("youtube_playlists: []")
    fm_lines.append(f'summary: "{summary}"')
    fm_lines.extend([
        'layout: "single"',
        'type: "uc"',
        'cascade:',
        '  type: "topic"',
        'contributors: []',
        "---",
        "",
        uc["body"].strip(),
        "",
    ])
    with open(os.path.join(dir_path, "_index.md"), "w", encoding="utf-8") as f:
        f.write("\n".join(fm_lines))
    for topic in uc["topics"]:
        topic_path = os.path.join(dir_path, f"{topic['slug']}.md")
        topic_title = _quote(topic["title"]).strip()
        topic_summary = _quote(topic["summary"]).strip()
        topic_lines = [
            "---",
            f'slug: "{topic["slug"]}"',
            f'title: "{topic_title}"',
            f'summary: "{topic_summary}"',
        ]
        tags = topic.get("tags") or []
        if tags:
            topic_lines.append("tags:")
            for tag in tags:
                topic_lines.append(f'  - "{_quote(tag)}"')
        else:
            topic_lines.append("tags: []")
        topic_lines.append("youtube_playlists: []")
        topic_lines.append("contributors: []")
        topic_lines.extend([
            "---",
            "",
            topic["body"].strip(),
            "",
        ])
        with open(topic_path, "w", encoding="utf-8") as f:
            f.write("\n".join(topic_lines))


def main():
    for uc in COURSE_PLAN:
        write_uc(uc)


if __name__ == "__main__":
    main()
